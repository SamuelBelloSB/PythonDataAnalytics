valorA, valorB = 10, 5

#   Adição
print(f"Adicao {valorA} + {valorB} = {valorA+valorB}")

#   Subtracao
print(f"Subtracao {valorA} - {valorB} = {valorA-valorB}")

#   Multiplicação
print(f"Multiplicacao {valorA} * {valorB} = {valorA*valorB}")

#   Divisão
print(f"Divisao {valorA} / {valorB} = {valorA/valorB}")

#   Divisao inteira
print(f"Divisao inteira {valorA} // {valorB} = {valorA//valorB}")

#   Resto da divisao/Módulo
print(f"Modulo/Resto da divisao {valorA} % {valorB} = {valorA%valorB}")

#   Exponenciação
print(f"Exponenciacao {valorA} ** {valorB} = {valorA**valorB}")